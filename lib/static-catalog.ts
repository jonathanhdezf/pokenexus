// Genera 150 cartas distribuyéndolas entre los sets
function generateCards(): StaticCard[] {
    return pokemonData.map((poke, i) => {
        const set = pokemonSets[i % pokemonSets.length];
        const cardNum = (i % 200) + 1;
        return {
            id: `${set.prefix}-${cardNum}`,
            name: poke.name,
            set: { name: set.name },
            types: poke.types,
            images: {
                large: `https://images.pokemontcg.io/${set.prefix}/${cardNum}_hires.png`,
                small: `https://images.pokemontcg.io/${set.prefix}/${cardNum}.png`,
            },
            rarity: poke.rarity,
            tcgplayer: { prices: { holofoil: { market: poke.price }, normal: { market: poke.price * 0.6 }, reverseHolofoil: { market: poke.price * 0.8 } } },
        };
    });
}

export const STATIC_CATALOG: StaticCard[] = generateCards();
